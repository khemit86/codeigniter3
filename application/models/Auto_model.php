    function getFeild ($select, $table, $feild = '', $value = '', $where = NULL, $limit_from = 0, $limit_to = 0)
    {
		try {
			$this->db->select ($select);
			if (($value != '' && $feild != ''))
			{
				if (0 < $limit_from)
				{
					$rs = $this->db->get_where ($table, [$feild => $value], $limit_to, $limit_from);
				}
				else
				{
					$rs = $this->db->get_where ($table, [$feild => $value]);
				}
			}
			else
			{
				if (0 < $limit_from)
				{
					$rs = $this->db->get_where ($table, $where, $limit_to, $limit_from);
				}
				else
				{
					$rs = $this->db->get_where ($table, $where);
				}
			}
			
		} catch(Exception $e) {
			print_r($e);
		}
        $data = NULL;

        foreach ($rs->result () as $row)
        {
            $data = $row->$select;
        }
		 return  ($data);
    }
	
  
   
