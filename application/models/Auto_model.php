    /**
     * Validate the current user session and returns the current User Role
     * @return type int Role if session active or redirect out of the site
     */
    public function validate_session ()
    {	
		
        if ($this->session->userdata ('is_logged') == TRUE)
        {
			/* echo "<pre>";
			print_r($this->session->userdata('user'));
			die;
			 */
			$uid = (int)$this->session->userdata('user')[0]->user_id;
			
			$verifi_user = $this->db
			->select ('user_id')
			//->from ('user') // old defination
			->from ('users') // new definations
			->where ('user_id', $uid)
			->get ()->row();	
			
			$non_verifi_user = $this->db
			->select ('user_id,verification_code','first_name')
			->from ('users_new_registrations_pending_verification')
			->where ('user_id', $uid)
			->get ()->row();
			
			
			
			if (!empty($non_verifi_user->user_id) && !$this->session->userdata('is_authorized'))
            {
				
				redirect(VPATH . $this->config->item('signup_activate_page_url').'?'.$this->config->item('signup_page_success_parameter')."=".Cryptor::doEncrypt($non_verifi_user->user_id));
				
                
            }else if(!empty($verifi_user->user_id)){
			
				return TRUE;
			}
            else
            {
				
               // if (current_url () != site_url () && current_url () != site_url ('login') && current_url () != site_url ('dashboard') && current_url () != site_url ('signup'))
